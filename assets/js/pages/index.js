document.addEventListener("DOMContentLoaded", function() {
    // 1. 精准抓取作为“触发开关”的 Line-up Bar 和右侧的描述文字
    const lineupBar = document.querySelector('.ui_top_bar');                     // 你的 Bar 容器类名
    const descElement = document.querySelector('.left_container .faction_desc'); // 你的描述文字类名

    if (lineupBar && descElement) {
        // 2. 你的文字内容（硬编码焊死，防止被其他切卡片逻辑冲掉）
        const myHardcodedText = `
        [Radiant clusters in the Wotagei Universe]
        The visualization of the order and rule`;
        
        // 3. 【初始状态设置】一进页面，强行清空内容并隐形
        descElement.innerHTML = '';
        descElement.style.opacity = '0';
        descElement.style.visibility = 'hidden';

        // 状态锁：整个生命周期只打字一次
        let hasTyped = false; 

        // 4. 打字机核心递归函数
        function typeWriter(text, index) {
            if (index < text.length) {
                const currentChar = text[index];
                
                if (currentChar === '\n') {
                    descElement.innerHTML += '<br>';
                } else {
                    descElement.innerHTML += currentChar;
                }
                
                // 30ms 敲击一个字，速度利落
                setTimeout(() => {
                    typeWriter(text, index + 1);
                }, 30); 
            }
        }

        // 5. 现代高级监听：死盯着 Line-up Bar 的落位状态
        if ('IntersectionObserver' in window) {
            const observer = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    // 当 Bar 滚动到了视口顶部预设的触发区域
                    if (entry.isIntersecting && !hasTyped) {
                        hasTyped = true; // 立刻关闸锁死
                        
                        // 右侧 desc 解除隐藏，优雅淡入显形
                        descElement.style.visibility = 'visible';
                        descElement.style.opacity = '1';
                        
                        // 开始打字
                        typeWriter(myHardcodedText, 0);

                        // 任务完成，当场拔掉对 Bar 的监听，释放浏览器内存
                        observer.unobserve(lineupBar);
                    }
                });
            }, {
                threshold: 0.0, // 只要 Bar 的任意一部分压线
                rootMargin: '0px 0px -95% 0px' // 【核心防线】当 Bar 触及屏幕顶端（向下滚动的落位点）瞬间精准点火
            });

            // 启动对 Bar 的暗中观察
            observer.observe(lineupBar);
        }
    }
});


