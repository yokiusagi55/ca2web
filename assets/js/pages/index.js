document.addEventListener("DOMContentLoaded", () => {
    const container = document.getElementById('format-card-swap');
    if (!container) return;

    let mobileAutoSlide = null;
    let desktopIntervalId = null;
    let currentMode = null; // 'mobile' | 'desktop'
    let mobileCurrent = 1;  // 移动端由于头部有缓存垫，从1开始
    let isMobileAnimating = false;
    let cardOrder = [];

    // 获取原始卡片
    const originalCards = Array.from(container.querySelectorAll('.card'));
    const totalOriginalCards = originalCards.length;
    if (totalOriginalCards === 0) return;

    /* ==================================================== */
    /* 🛠️ CORE - CLEANUP SYSTEM (解决拉伸、切换滞后 Bug)      */
    /* ==================================================== */
    function destroyAllEffects() {
        // 清理定时器
        if (mobileAutoSlide) { clearInterval(mobileAutoSlide); mobileAutoSlide = null; }
        if (desktopIntervalId) { clearInterval(desktopIntervalId); desktopIntervalId = null; }
        
        // 移除所有 GSAP 注入的内联属性与变换，交还给纯 CSS 控制
        gsap.killTweensOf(container);
        gsap.killTweensOf(container.querySelectorAll('.card'));
        
        // 移除克隆节点（如果存在）
        container.querySelectorAll('.card-clone').forEach(el => el.remove());
        
        // 还原所有卡片最原始的干净状态
        const currentCards = container.querySelectorAll('.card');
        gsap.set(currentCards, { clearProps: "all" });
        gsap.set(container, { clearProps: "all" });
        
        isMobileAnimating = false;
    }

    /* ==================================================== */
    /* 📱 MOBILE REBUILD - SEAMLESS LOOKAHEAD LOOP          */
    /* ==================================================== */
    function initMobileMode() {
        destroyAllEffects();
        currentMode = 'mobile';

        const cards = Array.from(container.querySelectorAll('.card'));
        
        // 1. 头尾双重缓冲克隆（Newsbar 无缝回弹核心）
        const firstClone = cards[0].cloneNode(true);
        const lastClone = cards[cards.length - 1].cloneNode(true);
        
        firstClone.classList.add('card-clone');
        lastClone.classList.add('card-clone');
        
        container.appendChild(firstClone); // 丢到最后
        container.insertBefore(lastClone, cards[0]); // 塞到最前

        const updatedCards = Array.from(container.querySelectorAll('.card'));
        mobileCurrent = 1; // 默认停在真实的第一张

        // 2. 动态居中计算（精准剔除 padding 与屏宽误差）
        function getTargetX(index) {
            const gap = 16;
            const stageWidth = container.parentElement.offsetWidth;
            const cardWidth = updatedCards[0].offsetWidth;
            
            // 计算单张卡片水平居中所需的偏移量
            const centerOffset = (stageWidth - cardWidth) / 2;
            
            // 移动端去掉了 padding-left，改用精准的坐标公式
            let positionX = 0;
            for(let i = 0; i < index; i++) {
                positionX += updatedCards[i].offsetWidth + gap;
            }
            return centerOffset - positionX;
        }

        // 初始校准到真实第一张（此时处于居中状态）
        gsap.set(container, { x: getTargetX(mobileCurrent) });

        function goNext() {
            if (isMobileAnimating) return;
            isMobileAnimating = true;

            mobileCurrent++;

            gsap.to(container, {
                x: getTargetX(mobileCurrent),
                duration: 0.8,
                ease: "power2.inOut",
                onComplete: () => {
                    // 当走到最后的克隆盘时，悄无声息瞬间斩回真实第一张
                    if (mobileCurrent >= updatedCards.length - 1) {
                        mobileCurrent = 1;
                        gsap.set(container, { x: getTargetX(mobileCurrent) });
                    }
                    isMobileAnimating = false;
                }
            });
        }

        mobileAutoSlide = setInterval(goNext, 3500);
    }

    /* ==================================================== */
    /* 💻 DESKTOP REBUILD - 3D CARD SWAP                     */
    /* ==================================================== */
    const config = {
        cardDistance: 60, verticalDistance: 70, delay: 5000, skewAmount: 6,
        ease: 'elastic.out(0.6, 0.9)', durDrop: 2, durMove: 2, durReturn: 2,
        promoteOverlap: 0.9, returnDelay: 0.05
    };

    function makeSlot(i) {
        return { x: i * config.cardDistance, y: -i * config.verticalDistance, z: -i * config.cardDistance * 1.5, zIndex: totalOriginalCards - i };
    }

    function initDesktopMode() {
        destroyAllEffects();
        currentMode = 'desktop';

        cardOrder = Array.from({ length: totalOriginalCards }, (_, i) => i);

        originalCards.forEach((el, i) => {
            const slot = makeSlot(i);
            gsap.set(el, { x: slot.x, y: slot.y, z: slot.z, xPercent: -50, yPercent: -50, skewY: config.skewAmount, transformOrigin: 'center center', zIndex: slot.zIndex, force3D: true });
        });

        startDesktopInterval();
    }

    function desktopSwap() {
        if (cardOrder.length < 2) return;
        const [front, ...rest] = cardOrder;
        const elFront = originalCards[front];
        const tl = gsap.timeline();

        tl.to(elFront, { y: '+=500', duration: config.durDrop, ease: config.ease });
        tl.addLabel('promote', `-=${config.durDrop * config.promoteOverlap}`);

        rest.forEach((idx, i) => {
            const el = originalCards[idx];
            const slot = makeSlot(i);
            tl.set(el, { zIndex: slot.zIndex }, 'promote');
            tl.to(el, { x: slot.x, y: slot.y, z: slot.z, duration: config.durMove, ease: config.ease }, `promote+=${i * 0.15}`);
        });

        const backSlot = makeSlot(totalOriginalCards - 1);
        tl.addLabel('return', `promote+=${config.durMove * config.returnDelay}`);
        tl.call(() => gsap.set(elFront, { zIndex: backSlot.zIndex }), null, 'return');
        tl.to(elFront, { x: backSlot.x, y: backSlot.y, z: backSlot.z, duration: config.durReturn, ease: config.ease }, 'return');
        tl.call(() => { cardOrder = [...rest, front]; });
    }

    function startDesktopInterval() { if (!desktopIntervalId) desktopIntervalId = setInterval(desktopSwap, config.delay); }
    function stopDesktopInterval() { clearInterval(desktopIntervalId); desktopIntervalId = null; }

    container.addEventListener('mouseenter', () => { if(currentMode === 'desktop') stopDesktopInterval(); });
    container.addEventListener('mouseleave', () => { if(currentMode === 'desktop') startDesktopInterval(); });


    /* ==================================================== */
    /* ⚖️ ENVIRONMENT WATCHER - 响应式分流监听器               */
    /* ==================================================== */
    function checkLayoutMode() {
        const isMobileWidth = window.innerWidth <= 768;
        if (isMobileWidth && currentMode !== 'mobile') {
            initMobileMode();
        } else if (!isMobileWidth && currentMode !== 'desktop') {
            initDesktopMode();
        }
    }

    // 初始化运行
    checkLayoutMode();

    // 监听视口改变，动态重置样式，抹平滞后 Bug
    let resizeTimeout;
    window.addEventListener('resize', () => {
        clearTimeout(resizeTimeout);
        resizeTimeout = setTimeout(checkLayoutMode, 200);
    });

    document.addEventListener("visibilitychange", () => {
        if (document.hidden) {
            if (mobileAutoSlide) clearInterval(mobileAutoSlide);
            if (desktopIntervalId) clearInterval(desktopIntervalId);
        } else {
            if (currentMode === 'mobile') initMobileMode();
            if (currentMode === 'desktop') startDesktopInterval();
        }
    });
});




