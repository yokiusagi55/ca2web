function moveTicker(direction) {
    if (isMoving || !tickerGroup) return;
    
    const n = tickerGroup.children.length;
    const visibleCount = window.innerWidth <= 768 ? 1 : 4;
    
    // 如果卡片不足以滚动（小于等于当前坑位数），直接跳过
    if (n <= visibleCount) return;

    isMoving = true;

    // --- 核心位置判断：a[1] 位置去找卡片 n-3 ---
    // 逻辑：如果现在已经显示了最后 4 张，下一次点击或自动轮播直接回 0
    if (direction > 0 && currentIndex >= n - visibleCount) {
        currentIndex = 0;
    } 
    else if (direction < 0 && currentIndex <= 0) {
        currentIndex = n - visibleCount;
    } 
    else {
        currentIndex += direction;
    }

    const step = getStep();
    tickerWrapper.style.transition = 'transform 0.6s cubic-bezier(0.2, 1, 0.3, 1)';
    tickerWrapper.style.transform = `translateX(-${currentIndex * step}px)`;

    setTimeout(() => { isMoving = false; }, 600);
}

function startAuto() {
    stopAuto();
    const n = tickerGroup ? tickerGroup.children.length : 0;
    const visibleCount = window.innerWidth <= 768 ? 1 : 4;
    if (n > visibleCount) {
        tickerTimer = setInterval(() => moveTicker(1), 5000); // 间隔 5s
    }
}

function stopAuto() {
    clearInterval(tickerTimer);
    tickerTimer = null;
}

function initTicker() {
    if (!tickerGroup) return;
    const n = tickerGroup.children.length;
    const visibleCount = window.innerWidth <= 768 ? 1 : 4;

    if (n > visibleCount) {
        tickerRoot.classList.add('is-rolling');
        startAuto();
    } else {
        tickerRoot.classList.remove('is-rolling');
        tickerWrapper.style.transform = 'translateX(0)';
        stopAuto();
    }
}

