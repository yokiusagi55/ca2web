window.initBaiduMap = function () {

    if (typeof BMapGL === 'undefined') return;

    const mapContainer =
        document.getElementById('baidu_map_container');

    if (!mapContainer) return;

    // ==========================================
    //  MAO Livehouse(武汉店) 固定坐标
    // ==========================================

    const VENUE = {

        name: "MAO Livehouse(武汉店)",

        lng: 114.29878510291213,

        lat: 30.592680862086286
    };

    // ==========================================
    // 初始化地图
    // ==========================================

    const mapInstance =
        new BMapGL.Map('baidu_map_container');

    // 创建 Point

    const point =
        new BMapGL.Point(
            VENUE.lng,
            VENUE.lat
        );

    // 地图中心定位

    mapInstance.centerAndZoom(point, 18);

    // 开启滚轮缩放

    mapInstance.enableScrollWheelZoom(true);

    // ==========================================
    // 原生红色 Marker
    // ==========================================

    const marker =
        new BMapGL.Marker(point);

    mapInstance.addOverlay(marker);

    // ==========================================
    // 地图风格（可删）
    // ==========================================

    mapInstance.setMapStyleV2({
        styleId: 'midnight'
    });

    // ==========================================
    // 防止移动端乱旋转
    // ==========================================

    mapInstance.disableRotate();

    mapInstance.disableTilt();

    console.log("地图初始化完成");
};


